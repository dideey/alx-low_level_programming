#include "3-calc.h"
/**
 * op_add - addition
 * Return:ans
 * @a:int
 * @b:int2
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - subtract
 * Return:ans
 * @a:int
 * @b:int2
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - multiplication
 * Return:ans
 * @a:int
 * @b:int2
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - division
 * Return:ans
 * @a:int
 * @b:int2
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - modulus
 * Return:ans
 * @a:int
 * @b:int2
 */
int op_mod(int a, int b)
{
return (a % b);
}

